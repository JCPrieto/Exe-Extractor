    private void jButton3ActionPerformed() {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String cad= rutaArchivo+" /x "+rutaSave;
            Runtime.getRuntime().exec(cad);
            Continuar c= new Continuar(new Frame(), true);
            jProgressBar1.setValue(50);
            c.setVisible(true);
            while (!fin) {
                Thread.onSpinWait();
            }
            String oldName = "%EXENAME%";
            File newFile = new File(rutaSave + System.getProperty("file.separator") + oldName);
            String newName = "Exe.zip";
            newFile.renameTo(new File(rutaSave + System.getProperty("file.separator") + newName));
            this.jTextArea1.setText(this.jTextArea1.getText()+"-El archivo que contiene lo que usted desea se llama Exe.zip");
            jProgressBar1.setValue(100);
        } 
        catch (Exception e) 
        { 
            // Se lanza una excepciï¿½n si no se encuentra en ejecutable o el fichero no es ejecutable.
            JOptionPane.showMessageDialog(new Frame(),"Imposible abrir el archivo.","Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed() {//GEN-FIRST:event_jButton2ActionPerformed
        SelectDir sd= new SelectDir();
        Container parent = this.getParent();
        int choice = sd.jFileChooser1.showOpenDialog(parent);
        if (choice == JFileChooser.APPROVE_OPTION){
            rutaSave= sd.jFileChooser1.getSelectedFile().getAbsolutePath();
        }
        this.jLabel2.setText(rutaSave);
    }//GEN-LAST:event_jButton2ActionPerformed

